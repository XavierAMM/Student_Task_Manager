    private void evaluarBotones() {
        ableToRight = false;
        ableToLeft = false;
        btnLeft.setEnabled(true);
        btnRight.setEnabled(true);
        if (cmbEstudiantes.getSelectedIndex() == 0) {
            btnLeft.setEnabled(false);
            ableToRight = true;
            ableToLeft = false;
        }
        if (cmbEstudiantes.getSelectedIndex() == cmbEstudiantes.getItemCount() - 1) {
            btnRight.setEnabled(false);
            ableToRight = false;
            ableToLeft = true;
        }
    }

    private void cargarEntrega() {
        txtTitulo.setText("");
        txtDescripcion.setText("");
        txtCalificacion.setText("");
        for (Entrega e : entregas) {
            Entrega entregaCmb = (Entrega) cmbEstudiantes.getSelectedItem();
            if (e.getIdEntrega() == entregaCmb.getIdEntrega()) {
                txtTitulo.setText(e.getTitulo());
                txtDescripcion.setText(e.getDescripcion());
                if (e.getCalificacion() >= 0) 
                    txtCalificacion.setText("" + e.getCalificacion());                
            }
        }
        cmbEstudiantes.requestFocusInWindow();
    }

