    private void btnCalificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalificarActionPerformed
        if(cmbEstudiantes.getItemCount()==0){
            JOptionPane.showMessageDialog(this, "No hay estudiantes por calificar.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String strCalificacion = txtCalificacion.getText().trim();
        int calificacion = 0;
        try{
            boolean quitarCalif = false;
            if(strCalificacion.equals("")) quitarCalif = true;
            else calificacion = Integer.parseInt(strCalificacion);
            if(calificacion < 0 || calificacion > 100) {
                JOptionPane.showMessageDialog(this, "No ha ingresado una calificación válida.","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(quitarCalif) calificacion = -1;
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "No ha ingresado una calificación válida.","Error",JOptionPane.ERROR_MESSAGE);
            txtCalificacion.setText("");
            txtCalificacion.requestFocusInWindow();
            return;
        }
        Entrega entregaSeleccionada = (Entrega) cmbEstudiantes.getSelectedItem();
        entregaSeleccionada.setCalificacion(calificacion);
        _ec.updateEntrega(entregaSeleccionada);
        cmbEstudiantes.requestFocusInWindow();        
        JOptionPane.showMessageDialog(this, "Calificación actualizada con éxito.","Información",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnCalificarActionPerformed

