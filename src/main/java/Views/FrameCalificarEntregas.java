    private void cargarComboBox() {
        comboboxCargado = false;
        txtCalificacion.setText("");
        entregas = _ec.getEntregasByIdTarea(tarea.getIdTarea());
        entregas.sort(Comparator
                .comparingInt(Entrega::getIdEntrega) // Primero por idEntrega
                .thenComparing(e -> e.getCalificacion() == null ? Integer.MAX_VALUE : e.getCalificacion())); // Luego por calificacion

        //Collections.reverse(entregas);
        DefaultComboBoxModel<Entrega> model = new DefaultComboBoxModel<>();

        if (!rdbVerTodos.isSelected()) {
            entregas = entregas.stream()
                    .filter(entrega -> entrega.getCalificacion() != null && entrega.getCalificacion() < 0)
                    .collect(Collectors.toList());
        }
        for (Entrega e : entregas) {
            model.addElement(e);
        }
        cmbEstudiantes.setModel(model);
        if (cmbEstudiantes.getItemCount() > 0) {
            comboboxCargado = true;
        }
        evaluarBotones();
        cargarEntrega();
    }

