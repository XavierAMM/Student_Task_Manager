    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar esta tarea? (Esta acción no se puede deshacer)", "Confirmación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.YES_OPTION){
            _ec.deleteEntregasByIdTarea(tarea.getIdTarea());
            _tc.deleteTarea(tarea.getIdTarea());            
            JOptionPane.showMessageDialog(this, "Tarea borrada con éxito.", "Información", JOptionPane.INFORMATION_MESSAGE);
            if(parent instanceof PanelTareas panelTareas) panelTareas.agregarTareas();
            else ((PanelCalificacionTareas) parent).cargarTabla();
            this.dispose();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

