    private void colocarPlaceHolders(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");        
        txtFechaApertura.setText(LocalDateTime.now().format(dtf));
        txtFechaEntrega.setText(LocalDateTime.now().format(dtf));
        Configurations.addPlaceholder(txtTitle, "Título");
        Configurations.addPlaceholder(txtDescripcion, "Descripción");
    }
    
    private void cargarDatosTarea(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        txtFechaApertura.setText(tarea.getFechaCreacion().format(dtf));
        txtFechaEntrega.setText(tarea.getFechaEntrega().format(dtf));
        txtTitle.setText(tarea.getTitulo());
        txtDescripcion.setText(tarea.getDescripcion());
        List<Entrega> entregas = _ec.getEntregasByIdTarea(tarea.getIdTarea());
        btnVerEntregas.setText("Ver Entregas (" + entregas.size() + ")");
        if(entregas.isEmpty()) btnVerEntregas.setEnabled(false);
    }
    
    

