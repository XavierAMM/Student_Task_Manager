    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Tarea nuevaTarea;
        String msg;
        try{
           nuevaTarea = obtenerDatosTarea();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(modo == 1){ // Edicion
            nuevaTarea.setIdTarea(tarea.getIdTarea());
           _tc.updateTarea(nuevaTarea);
           msg = "Tarea actualizada con éxito.";
        }else{ // Nueva
            _tc.addTarea(nuevaTarea);
            msg = "Tarea creada con éxito";
        }
        
        JOptionPane.showMessageDialog(this, msg, "Información", JOptionPane.INFORMATION_MESSAGE); 
        if(parent instanceof PanelTareas panelTareas) panelTareas.agregarTareas();
        else ((PanelCalificacionTareas) parent).cargarTabla();
        this.dispose();        
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    private Tarea obtenerDatosTarea() throws Exception{
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        LocalDateTime fechaApertura;
        LocalDateTime fechaEntrega;
        String strFechaApertura = txtFechaApertura.getText().trim();
        String strFechaEntrega = txtFechaEntrega.getText().trim();
        String titulo = txtTitle.getText();
        String descripcion = txtDescripcion.getText();
        
        if(strFechaApertura.equals("") ||
           strFechaEntrega.equals("") ||
           titulo.equals("") ||
           descripcion.equals(""))
            throw new Exception("Rellene todos los campos.");        
        
        try{
            fechaApertura = LocalDateTime.parse(strFechaApertura, dtf);
            fechaEntrega = LocalDateTime.parse(strFechaEntrega, dtf);
        }catch(Exception e){
            throw new Exception("El formato de la fecha es incorrecto. Use 'dd/MM/yyyy HH:mm'.");
        }

        if(fechaApertura.isAfter(fechaEntrega))
            throw new Exception("La fecha y hora de entrega no puede ser menor a la de apertura.");
        
        return new Tarea(0, idMateria, titulo, descripcion, fechaApertura, fechaEntrega);
    }
    
